package coeffectChecking.Coeffect;

/**
 * L'enumerazione Coef rappresenta i possibili stati di coeffetto associati a una classe.
 * - COEFF: Indica che la classe ha un coeffetto principale.
 * - AUXCOEF: Indica che la classe ha un coeffetto ausiliario.
 * - NOTCOEF: Indica che la classe non ha alcun coeffetto associato.
 */
public enum Coef {
    COEFF,    // Indica che la classe ha un coeffetto principale.
    AUXCOEF,  // Indica che la classe ha un coeffetto ausiliario.
    NOTCOEF   // Indica che la classe non ha alcun coeffetto associato.
}