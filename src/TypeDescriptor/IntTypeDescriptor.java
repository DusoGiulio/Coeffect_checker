package TypeDescriptor;

/**
 * Questa classe rappresenta un descrittore di tipo per il tipo intero (int).
 */
public class IntTypeDescriptor extends TypeDescriptor {

    /**
     * Restituisce una rappresentazione testuale del descrittore di tipo intero.
     *
     * @return La stringa "int" (che rappresenta il tipo intero).
     */
    @Override
    public String toString() {
        return "int";
    }
}
