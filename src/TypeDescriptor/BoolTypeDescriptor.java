package TypeDescriptor;

/**
 * Questa classe rappresenta un descrittore di tipo per il tipo booleano.
 */
public class BoolTypeDescriptor extends TypeDescriptor {

    /**
     * Restituisce una rappresentazione testuale del descrittore di tipo booleano.
     *
     * @return La stringa "boolean" (che rappresenta il tipo booleano).
     */
    @Override
    public String toString() {
        return "boolean";
    }
}